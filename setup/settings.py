def _carregar_vapid_private_key(valor: str) -> str:
    """Aceita conteúdo PEM ou caminho para arquivo .pem na VPS."""
    valor = (valor or '').strip()
    if not valor:
        return ''
    caminho = Path(valor)
    if caminho.is_file():
        return caminho.read_text(encoding='utf-8').strip()
    return valor


