def _normalizar_static_url(url: str) -> str:
    """Garante URL absoluta (/static/) para o admin não gerar caminhos sob /admin/login/."""
    url = (url or '/static/').strip()
    if not url.startswith('/'):
        url = f'/{url}'
    if not url.endswith('/'):
        url = f'{url}/'
    return url


