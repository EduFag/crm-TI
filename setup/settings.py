def _carregar_arquivo_env(caminho: Path) -> int:
    """Carrega variáveis KEY=VALUE do arquivo .env para os.environ. Retorna quantas chaves foram lidas."""
    if not caminho.exists():
        return 0
    carregadas = 0
    with caminho.open(encoding='utf-8') as arquivo:
        for linha in arquivo:
            linha = linha.strip()
            if not linha or linha.startswith('#') or '=' not in linha:
                continue
            chave, valor = linha.split('=', 1)
            chave = chave.strip()
            valor = valor.strip().strip('"').strip("'")
            if chave:
                antes = chave not in os.environ
                os.environ.setdefault(chave, valor)
                if antes and chave in os.environ:
                    carregadas += 1
    return carregadas


